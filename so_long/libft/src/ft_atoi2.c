long	ft_atoi2(const char *str)
{
	long	k;
	long	n;

	k = 1;
	n = 0;
	while (*str && ((*str >= 9 && *str <= 13) || *str == ' '))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			k = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + k * (*str - '0');
		str++;
	}
	if (*str && *str != '.')
		return (3000000000);
	if (*str == '.' && *(str + 1) >= '0' && *(str + 1) <= '9')
		return (3000000002);
	if (n > 2147483647 || n < -2147483648)
		return (3000000001);
	return (n);
}
